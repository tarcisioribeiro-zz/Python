# Recebe o nome da pessoa
nome = str(input('Diga o seu nome: '))

# Bloco de condição
if nome == 'Gustavo':  # Condição para verdadeiro
    print('Que nome lindo você tem!')
else:  # Condição para falso
    print('Seu nome é tão normal!')

# Mostra na tela o nome recebido na váriavel nome
print('Bom dia, {}!'.format(nome))

